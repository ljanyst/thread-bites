#include "tb.h"

//------------------------------------------------------------------------------
// Get the pointer of the currently running thread
//------------------------------------------------------------------------------
tbthread_t tbthread_self()
{
  tbthread_t self;
  asm("movq %%fs:0, %0\n\t" : "=r" (self));
  return self;
}
